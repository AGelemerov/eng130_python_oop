# multiplies two numbers
def __multiply(num1, num2):
    return num1 * num2


# divides two numbers
def __divide(num1, num2):
    return num1 / num2


# executes the modulo operator on two numbers
def __modulus(num1, num2):
    return num1 % num2


# converts centimeters to meters
def __cm_to_m(cm):
    return cm / 100


# converts inches to centimeters
def __inch_to_cm(inch):
    return inch * 2.25


# converts meters to feet
def __m_to_feet(m):
    return m * 3.28
